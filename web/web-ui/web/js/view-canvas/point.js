/**
 * View Canvas Point
 *
 * @param {number} x
 * @param {number} y
 * @constructor
 */
function ViewCanvasPoint( x, y )
{
	/**
	 * X-coordinate
	 *
	 * @type {number}
	 */
	this.mX = x;

	/**
	 * Y-coordinate
	 *
	 * @type {number}
	 */
	this.mY = y;

	return;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

/**
 * Calculate the distance from this point to the given other point
 *
 * @param {ViewCanvasPoint} other
 * @return {number}
 */
ViewCanvasPoint.prototype.distanceFrom = function( other )
{
	return Math.sqrt(
		( this.mX - other.mX ) * ( this.mX - other.mX )
		+ ( this.mY - other.mY ) * ( this.mY - other.mY )
	);
};

